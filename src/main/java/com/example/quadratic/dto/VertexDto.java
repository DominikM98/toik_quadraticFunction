package com.example.quadratic.dto;

public class VertexDto {

    private double p;
    private double q;

    public VertexDto(double p, double q) {
    }

    public double getP() {
        return p;
    }

    public void setP(double p) {
        this.p = p;
    }

    public double getQ() {
        return q;
    }

    public void setQ(double q) {
        this.q = q;
    }
}
